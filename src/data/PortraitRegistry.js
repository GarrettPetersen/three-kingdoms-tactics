export function normalizePortraitKey(value) {
    return String(value || '').trim().toLowerCase();
}

export function resolvePortraitId(step = {}) {
    const rawSpeaker = String(step.speaker || '').trim();
    const speakerKey = rawSpeaker.toLowerCase();
    const nameKey = String(step.name || '').trim().toLowerCase();
    const explicitKey = normalizePortraitKey(step.portraitKey);
    const rawPortraitId =
        explicitKey ||
        SPEAKER_TO_PORTRAIT_ID[speakerKey] ||
        NAME_TO_PORTRAIT_ID[nameKey] ||
        rawSpeaker ||
        '';
    let resolvedPortraitId = LEGACY_PORTRAIT_ALIASES[normalizePortraitKey(rawPortraitId)]
        || normalizePortraitKey(rawPortraitId)
        || null;

    if (nameKey === 'yellow turban' || speakerKey === 'yellowturban') {
        resolvedPortraitId = 'yellow-turban';
    }
    return resolvedPortraitId;
}

export function getPortraitAssetKey(portraitId) {
    const resolved = LEGACY_PORTRAIT_ALIASES[normalizePortraitKey(portraitId)]
        || normalizePortraitKey(portraitId);
    return PORTRAIT_ASSET_KEY_BY_ID[resolved] || null;
}

export function getSpriteKeyForPortrait(portraitId) {
    const resolved = LEGACY_PORTRAIT_ALIASES[normalizePortraitKey(portraitId)]
        || normalizePortraitKey(portraitId);
    const compact = resolved.replace(/-/g, '').toLowerCase();
    return PORTRAIT_TO_SPRITE_KEY[resolved] || PORTRAIT_TO_SPRITE_KEY[compact] || null;
}

export function getSpeakerDialogueDefault(speaker) {
    return SPEAKER_DIALOGUE_DEFAULTS[normalizePortraitKey(speaker)] || null;
}

export function isNarratorPortrait(step = {}, portraitId = null) {
    const id = normalizePortraitKey(portraitId || step.portraitKey);
    const name = normalizePortraitKey(step.name);
    return id === 'narrator' || !step.name || name === 'narrator';
}

export function isNoticeboardPortrait(step = {}, portraitId = null) {
    const id = normalizePortraitKey(portraitId || step.portraitKey);
    const name = normalizePortraitKey(step.name);
    return id === 'noticeboard' || name.includes('noticeboard');
}
